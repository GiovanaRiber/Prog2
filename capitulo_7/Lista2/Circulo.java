package capitulo_7.Lista2;

public class Circulo {
    
    private double raio;
    private double ponto;
    
    public Circulo(){
        ponto = 0.0;
        raio = 1.0;
    }
    
    public Circulo(double ponto, double raio){
        this.raio = raio;
        this.ponto = ponto;
    }
    
}
